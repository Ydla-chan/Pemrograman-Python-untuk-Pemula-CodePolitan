a = int(input("Masukan Nilai A = "))
b = int(input("Masukan Nilai B = "))

# Operasi tambah
tambah = a + b

# Operasi Pengurangan
kurangan = a - b

# Operasi Perkalian
perkalian = a * b

# Operasi pembagian
pembagian = a / b

# Operasi mencari hasil bagi
SisaBagi = a % b


# Print Hasil Operasi Aritmasika
print("\n === Program pengenalan Operasi Aritmatika === ")
print("hasil dari pertambahan dari A dan B = ",tambah)
print("hasil dari pengurangan dari A dan B = ",kurangan)
print("hasil dari perkalian   dari A dan B = ",perkalian)
print("hasil dari Pembagian   dari A dan B = ",pembagian)
print("hasil dari Sisa Bagi   dari A dan B = ",SisaBagi)
